## IF Statements

# Simple IF Statement
age = 19

if age >= 18:
    print("You are old enough to vote!")
    print("Have you resgistered to vote yet?\n")


#IF-ELSE Statement
age = 17

if age >= 18:
    print("You are old enough to vote!")
    print("Have you resgistered to vote yet?\n")
else:
    print("Sorry! You are too young to vote")
    print("Please register to vote as soon as you turn 18!\n")

